package com.comcast.ifstatement;

public class HomeWorkIfStatement1 {

	public static void main(String[] args) {
		ifElse();
	}

	public static void ifElse() {

		int x = 10;
		int y = 25;
		if (x == y) {
			System.out.println(" x == y");

		}
		if (x != y) {
			System.out.println("x!=y ");

		}

		if (x >= y) {
			System.out.println("x>=y");

		}
		if (x <= y) {
			System.out.println("x <=y");

		}

	}

}
